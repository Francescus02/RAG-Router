COLORS = {
    "Always_Retrieve": "#888888", "Entropy": "#2196F3",
    "Entropy_PP": "#9C27B0",      "BM25_Always": "#FF9800",
}


def plot_em_distributions(results: Dict[str, Dict[str, Dict]],
                          bootstrap_rows: List[Dict],
                          output_path: str):
    """Forest plot (dot-whisker) of EM with 95% bootstrap CI per configuration."""
    if not MATPLOTLIB_AVAILABLE: return

    boot_map = {r["combination"]: r for r in bootstrap_rows}
    combos   = sorted(results.keys(), key=lambda x: boot_map.get(x,{}).get("em_mean",0))

    fig, axes = plt.subplots(1, 2, figsize=(14, max(6, len(combos)*0.5+2)))

    # Panel 1: global EM forest plot
    ax = axes[0]
    y  = np.arange(len(combos))

    for i, combo in enumerate(combos):
        b = boot_map.get(combo, {})
        if not b: continue
        em   = b.get("em_mean", 0)
        lo   = b.get("em_ci95_lo", em)
        hi   = b.get("em_ci95_hi", em)
        pre  = combo.split(" × ")[0] if " × " in combo else combo
        color = COLORS.get(pre, "#666666")

        ax.plot([lo, hi], [i, i], color=color, lw=2.5, alpha=0.7)
        ax.scatter([em], [i], color=color, s=60, zorder=3)

    ax.set_yticks(y)
    ax.set_yticklabels([c.replace(" × ","\n×") for c in combos], fontsize=8)
    ax.set_xlabel("Exact Match (EM) — 95% Bootstrap CI", fontsize=11)
    ax.set_title("Forest Plot: EM per configuration\n(bars = 95% CI)", fontsize=11)
    ax.grid(axis="x", alpha=0.3)
    ax.spines[["top","right"]].set_visible(False)

    # Baseline reference line
    baseline = "Always_Retrieve × Always_FAISS"
    bbase    = boot_map.get(baseline, {})
    if bbase:
        ax.axvline(x=bbase.get("em_mean",0), color="red", linestyle="--",
                   lw=1.2, alpha=0.5, label="Baseline")
        ax.legend(fontsize=9)

    # Panel 2: EM per bucket with 95% CI
    ax2    = axes[1]
    combos_sel = [c for c in combos if "BM25_Always" not in c]
    x      = np.arange(3)
    width  = 0.8 / max(len(combos_sel), 1)
    
    for i, combo in enumerate(combos_sel):
        b   = boot_map.get(combo, {})
        pre = combo.split(" × ")[0] if " × " in combo else combo
        color = COLORS.get(pre, "#666666")
        for j, bucket in enumerate(BUCKET_ORDER):
            em_b  = b.get(f"em_{bucket}")
            lo_b  = b.get(f"em_{bucket}_ci_lo")
            hi_b  = b.get(f"em_{bucket}_ci_hi")
            if em_b is None: continue
            pos = j + (i - len(combos_sel)/2) * width
            ax2.bar(pos, em_b, width*0.9, color=color, alpha=0.6,
                    edgecolor="white", linewidth=0.3)
            if lo_b is not None:
                ax2.errorbar(pos, em_b, yerr=[[em_b-lo_b],[hi_b-em_b]],
                             fmt="none", color="black", capsize=2, lw=1)

    ax2.set_xticks(np.arange(3))
    ax2.set_xticklabels([f"HIGH\n(s_pop≥1000)","MEDIUM\n(100-999)","LOW\n(<100)"],
                        fontsize=9)
    ax2.set_ylabel("EM", fontsize=11)
    ax2.set_title("EM per s_pop bucket ± 95% CI\n(without BM25_Always)", fontsize=11)
    ax2.grid(axis="y", alpha=0.3)
    ax2.spines[["top","right"]].set_visible(False)

    import matplotlib.patches as mpatches
    patches = [mpatches.Patch(color=c, label=n, alpha=0.7)
               for n, c in COLORS.items() if n != "BM25_Always"]
    ax2.legend(handles=patches, fontsize=8, loc="upper right")

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"[PLOT] {output_path}")


def plot_mcnemar_heatmap(mcnemar_rows: List[Dict], output_path: str):
    """Heatmap of McNemar p-values."""
    if not MATPLOTLIB_AVAILABLE or not mcnemar_rows: return

    combos_b = sorted(set(r["system_b"] for r in mcnemar_rows))
    baseline  = mcnemar_rows[0]["system_a"] if mcnemar_rows else ""
    n = len(combos_b)
    if n == 0: return

    pvals   = np.ones((1, n))
    deltas  = np.zeros((1, n))

    for j, cb in enumerate(combos_b):
        for row in mcnemar_rows:
            if row["system_b"] == cb:
                pvals[0,j]  = row["pvalue"]
                deltas[0,j] = row["delta_em"]
                break

    fig, ax = plt.subplots(figsize=(max(8, n*1.5), 3))
    im = ax.imshow(pvals, cmap="RdYlGn_r", vmin=0, vmax=0.2, aspect="auto")

    for j in range(n):
        pv = pvals[0,j]; de = deltas[0,j]
        sig = "★" if pv < ALPHA else ""
        ax.text(j, 0, f"p={pv:.3f}\nΔ={de:+.3f}{sig}",
                ha="center", va="center", fontsize=8, fontweight="bold")

    ax.set_xticks(range(n))
    ax.set_xticklabels([c.replace(" × ","\n×") for c in combos_b],
                       fontsize=7.5, rotation=30, ha="right")
    ax.set_yticks([0])
    ax.set_yticklabels([f"vs\n{baseline[:30]}..."], fontsize=9)
    ax.set_title(f"McNemar Test p-values vs baseline\n"
                 f"(green=low p, ★=significant at α={ALPHA})", fontsize=11)

    plt.colorbar(im, ax=ax, label="p-value", shrink=0.8)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"[PLOT] {output_path}")


