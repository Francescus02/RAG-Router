# SECTION 2: TEST 1 — McNemar
# ═════════════════════════════════════════════════════════════════════════════

def mcnemar_test(em_a: List[float], em_b: List[float]) -> Dict:
    """
    McNemar test for paired comparison of binary classifiers.
    """
    assert len(em_a) == len(em_b), "Both lists must have the same length"

    a = b = c = d = 0
    for ea, eb in zip(em_a, em_b):
        ia, ib = int(round(ea)), int(round(eb))
        if   ia==1 and ib==1: a += 1
        elif ia==1 and ib==0: b += 1
        elif ia==0 and ib==1: c += 1
        else:                  d += 1

    n_disc = b + c

    if n_disc == 0:
        return {
            "statistic": 0.0, "pvalue": 1.0, "b": b, "c": c,
            "n_discordant": 0, "n_total": len(em_a),
            "interpretation": "Identical (no discordant queries)",
            "method": "none",
        }

    if n_disc < 25:
        # Exact binomial test: H₀: P(B=1|discordant) = 0.5
        pvalue = 2 * sp_stats.binom.sf(max(b,c)-1, n_disc, 0.5)
        pvalue = min(pvalue, 1.0)
        statistic = float(b) / n_disc   # Proportion (not χ²)
        method = "exact_binomial"
    else:
        # χ² with Edwards continuity correction
        statistic = (abs(b-c) - 1)**2 / n_disc
        pvalue    = sp_stats.chi2.sf(statistic, df=1)
        method    = "chi2_continuity"

    significant = pvalue < ALPHA
    direction   = "A > B" if b > c else "B > A" if c > b else "="

    return {
        "statistic":     round(float(statistic), 6),
        "pvalue":        round(float(pvalue), 6),
        "b":             b,   # A wins
        "c":             c,   # B wins
        "n_discordant":  n_disc,
        "n_total":       len(em_a),
        "pct_discordant":round(100*n_disc/len(em_a), 1),
        "direction":     direction,
        "significant":   significant,
        "method":        method,
        "interpretation":f"{'SIGNIFICANT' if significant else 'non sig.'} "
                         f"(p={pvalue:.4f}, α={ALPHA}, {direction})",
    }


def run_all_mcnemar(results: Dict[str, Dict[str, Dict]],
                    baseline: str) -> List[Dict]:
    """Run McNemar of every configuration against the baseline."""
    combos = sorted(results.keys())
    rows   = []

    print(f"\n[MCNEMAR] Baseline: '{baseline}'")
    print(f"          {len(combos)-1} comparisons to run\n")

    for combo in combos:
        if combo == baseline: continue

        shared = get_shared_queries(results, baseline, combo)
        if len(shared) < 10:
            print(f"  SKIP {combo}: only {len(shared)} shared queries")
            continue

        em_base  = [results[baseline][q]["em"] for q in shared]
        em_combo = [results[combo][q]["em"]     for q in shared]

        test     = mcnemar_test(em_base, em_combo)
        em_a_mu  = round(np.mean(em_base),  4)
        em_b_mu  = round(np.mean(em_combo), 4)

        row = {
            "system_a":      baseline,
            "system_b":      combo,
            "n_shared":      len(shared),
            "em_a":          em_a_mu,
            "em_b":          em_b_mu,
            "delta_em":      round(em_b_mu - em_a_mu, 4),
            **test,
        }
        rows.append(row)

        sig_marker = "★" if test["significant"] else "○"
        print(f"  {sig_marker} {combo[:42]:<42} | "
              f"ΔEM={em_b_mu-em_a_mu:+.4f} | "
              f"p={test['pvalue']:.4f} | disc={test['n_discordant']} | "
              f"{test['direction']}")

    return rows


# ═════════════════════════════════════════════════════════════════════════════
# SECTION 3: TEST 2 — Bootstrap CI
# ═════════════════════════════════════════════════════════════════════════════

def bootstrap_ci(values: List[float],
                 n_bootstrap: int = N_BOOTSTRAP,
                 alpha: float = ALPHA,
                 seed: int = RANDOM_SEED) -> Tuple[float, float, float]:
    """Bootstrap confidence interval for the mean of a distribution."""
    rng = np.random.RandomState(seed)
    arr = np.array(values, dtype=np.float64)
    n   = len(arr)

    boot_means = np.array([
        np.mean(rng.choice(arr, size=n, replace=True))
        for _ in range(n_bootstrap)
    ])

    ci_lo = float(np.percentile(boot_means, 100*alpha/2))
    ci_hi = float(np.percentile(boot_means, 100*(1-alpha/2)))
    return float(np.mean(arr)), ci_lo, ci_hi


def run_bootstrap_for_all(results: Dict[str, Dict[str, Dict]]) -> List[Dict]:
    """Bootstrap CI for EM and F1 of each combination, overall and per bucket."""
    print(f"\n[BOOTSTRAP] {N_BOOTSTRAP} resamples per combination...")
    rows = []

    for combo in sorted(results.keys()):
        q_data = list(results[combo].values())
        ems    = [r["em"] for r in q_data]
        f1s    = [r["f1"] for r in q_data]
        n      = len(ems)

        em_mean, em_lo, em_hi = bootstrap_ci(ems)
        f1_mean, f1_lo, f1_hi = bootstrap_ci(f1s)

        row = {
            "combination":   combo,
            "n":             n,
            "em_mean":       round(em_mean, 4),
            "em_ci95_lo":    round(em_lo, 4),
            "em_ci95_hi":    round(em_hi, 4),
            "em_ci95_width": round(em_hi - em_lo, 4),
            "f1_mean":       round(f1_mean, 4),
            "f1_ci95_lo":    round(f1_lo, 4),
            "f1_ci95_hi":    round(f1_hi, 4),
        }

        # Bootstrap per bucket
        for bucket in BUCKET_ORDER:
            b_ems = [r["em"] for r in q_data if r.get("pop_bucket")==bucket]
            if len(b_ems) >= 5:
                bm, bl, bh = bootstrap_ci(b_ems)
                row[f"em_{bucket}"]       = round(bm, 4)
                row[f"em_{bucket}_ci_lo"] = round(bl, 4)
                row[f"em_{bucket}_ci_hi"] = round(bh, 4)
            else:
                row[f"em_{bucket}"] = row[f"em_{bucket}_ci_lo"] = row[f"em_{bucket}_ci_hi"] = None

        rows.append(row)
        print(f"  {combo[:42]:<42}: EM={em_mean:.4f} [{em_lo:.4f},{em_hi:.4f}]  "
              f"F1={f1_mean:.4f} [{f1_lo:.4f},{f1_hi:.4f}]")

    return rows


# ═════════════════════════════════════════════════════════════════════════════
# SECTION 4: TEST 3 — Wilcoxon per bucket
# ═════════════════════════════════════════════════════════════════════════════

def run_wilcoxon_bucket(results: Dict[str, Dict[str, Dict]],
                        combo_a: str, combo_b: str) -> Dict:
    """Wilcoxon Signed-Rank test for paired comparison per s_pop bucket."""
    shared = get_shared_queries(results, combo_a, combo_b)
    bucket_results = {}

    for bucket in BUCKET_ORDER:
        shared_b = [q for q in shared
                    if results[combo_a][q].get("pop_bucket") == bucket]
        if len(shared_b) < 10:
            bucket_results[bucket] = {"pvalue": None, "n": len(shared_b)}
            continue

        em_a = [results[combo_a][q]["em"] for q in shared_b]
        em_b = [results[combo_b][q]["em"] for q in shared_b]
        diff = [a-b for a,b in zip(em_a, em_b)]

        if all(d == 0 for d in diff):
            bucket_results[bucket] = {"pvalue": 1.0, "n": len(shared_b),
                                       "direction": "="}
            continue

        try:
            stat, pval = sp_stats.wilcoxon(diff, alternative="two-sided",
                                            correction=True)
            direction = "A>B" if np.mean(diff)>0 else "B>A"
        except ValueError:
            pval, stat, direction = 1.0, 0.0, "="

        bucket_results[bucket] = {
            "pvalue":    round(float(pval), 6),
            "statistic": round(float(stat), 4),
            "n":         len(shared_b),
            "direction": direction,
            "significant": pval < ALPHA,
        }

    return bucket_results


