"""
p4b_statistical_tests.py
=========================
Formal statistical tests for PopQA ablation study.

  TEST 1 — McNemar Test (paired comparison between configurations):
    Compares two systems on the SAME queries. Requires only ~30 
    discordant queries to achieve sufficient statistical power (p<0.05).
    Standard in NLP for classifier comparison on identical test sets
    (Dror et al., 2018, "Hitchhiker's Guide to Testing Statistical
    Significance in NLP", ACL 2018).

    Statistic: χ²=(b-c)²/(b+c) with b=disagree(A=1,B=0), c=disagree(A=0,B=1)
    Distribution: χ²(df=1) under H₀ (both systems have same error rate)
    Correction: Edwards continuity correction (b+c≥25) or exact (b+c<25)

  TEST 2 — Bootstrap Confidence Intervals (95% CI on EM/F1):
    Resampling with replacement: 10,000 bootstrap samples.
    95% CI = percentiles [2.5, 97.5] of the bootstrap distribution.
    More reliable than normal approximation for small samples.
    Reference: Efron & Hastie (2016), Computer Age Statistical Inference.

  TEST 3 — Wilcoxon Signed-Rank Test (non‑parametric alternative):
    Compares EM distributions per s_pop bucket between configurations.
    Does not assume normality — appropriate for binary EM {0,1}.

OUTPUT:
  results/statistical_tests/mcnemar_results.csv
  results/statistical_tests/bootstrap_ci.csv
  results/statistical_tests/statistical_report.txt
  results/statistical_tests/fig_em_distributions.png
"""

