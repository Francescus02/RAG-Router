def write_statistical_report(mcnemar_rows: List[Dict],
                              bootstrap_rows: List[Dict],
                              path: str):
    lines = []
    lines.append("=" * 72)
    lines.append("FORMAL STATISTICAL TESTS — PopQA Ablation Study")
    lines.append("=" * 72)
    lines.append(f"CSV: {CSV_INPUT}")
    lines.append(f"α = {ALPHA}   Bootstrap: {N_BOOTSTRAP} resamples   Seed: {RANDOM_SEED}")
    lines.append("")

    lines.append("TEST 1 — McNemar (paired comparison vs baseline)")
    lines.append("─" * 72)
    lines.append(f"  {'System B':<42} | {'ΔEM':>6} | {'p-val':>8} | {'disc':>5} | {'Sig?':>5}")
    lines.append("  " + "─" * 68)
    n_sig = 0
    for row in sorted(mcnemar_rows, key=lambda x: x["pvalue"]):
        sig = "★ YES" if row["significant"] else "NO"
        if row["significant"]: n_sig += 1
        lines.append(
            f"  {row['system_b'][:42]:<42} | {row['delta_em']:>+6.4f} | "
            f"{row['pvalue']:>8.4f} | {row['n_discordant']:>5} | {sig:>5}"
        )
    lines.append(f"\n  Configurations significantly different from baseline: {n_sig}/{len(mcnemar_rows)}")
    lines.append("  ★ = p < 0.05 with Edwards continuity correction (or exact binomial test)")
    lines.append("")

    lines.append("TEST 2 — Bootstrap 95% CI (10,000 resamples)")
    lines.append("─" * 72)
    lines.append(f"  {'Combination':<42} | {'EM':>6} | {'CI95_lo':>8} | {'CI95_hi':>8} | {'Width':>6}")
    lines.append("  " + "─" * 72)
    for row in sorted(bootstrap_rows, key=lambda x: x["em_mean"], reverse=True):
        lines.append(
            f"  {row['combination'][:42]:<42} | {row['em_mean']:>6.4f} | "
            f"{row['em_ci95_lo']:>8.4f} | {row['em_ci95_hi']:>8.4f} | "
            f"{row['em_ci95_width']:>6.4f}"
        )
    lines.append("")

    lines.append("SCIENTIFIC INTERPRETATION")
    lines.append("─" * 72)
    lines.append(f"  Typical 95% CI width: ±{np.mean([r['em_ci95_width'] for r in bootstrap_rows])/2:.4f}")
    lines.append(f"  → EM differences < {np.mean([r['em_ci95_width'] for r in bootstrap_rows])/2:.4f}")
    lines.append(f"    are NOT statistically distinguishable.")
    lines.append("")
    lines.append("  The McNemar test is more sensitive because it leverages query pairing:")
    lines.append("  two systems with identical global EM can have different error patterns")
    lines.append("  (different queries), detectable by McNemar.")
    lines.append("")
    if n_sig > 0:
        lines.append(f"  ★ {n_sig} configuration(s) show(s) SIGNIFICANT difference")
        lines.append(f"    from baseline (McNemar p<{ALPHA}).")
    else:
        lines.append(f"  ○ No configuration shows significant difference")
        lines.append(f"    from baseline (McNemar p<{ALPHA}).")

    report = "\n".join(lines)
    with open(path, "w", encoding="utf-8") as f:
        f.write(report)
    print(f"[SAVE] Report: {path}")
    print("\n" + report)


