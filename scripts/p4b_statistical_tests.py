def safe_float(v: str) -> Optional[float]:
    try:
        f = float(v)
        return f if math.isfinite(f) else None
    except (ValueError, TypeError):
        return None


def load_results(csv_path: str) -> Dict[str, Dict[str, Dict]]:
    """
    Load results from CSV and organise them as:
    {combination: {query_id: {em, f1, s_pop, pop_bucket, route}}}
    """
    print(f"[LOAD] {csv_path}")
    results: Dict[str, Dict[str, Dict]] = defaultdict(dict)

    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            pre   = row.get("pre_strategy","")
            post  = row.get("post_strategy","")
            qid   = row.get("query_id","")
            combo = f"{pre} × {post}"

            em  = safe_float(row.get("em",""))
            f1  = safe_float(row.get("f1",""))
            if em is None or not qid: continue

            results[combo][qid] = {
                "em":         em,
                "f1":         f1 or 0.0,
                "s_pop":      safe_float(row.get("s_pop","1")) or 1.0,
                "pop_bucket": row.get("pop_bucket","unknown"),
                "route":      row.get("route_taken",""),
            }

    n_combos = len(results)
    n_q      = max(len(v) for v in results.values()) if results else 0
    print(f"[LOAD] {n_combos} combinations, max {n_q} queries per combination.")
    return dict(results)


def get_shared_queries(results: Dict[str, Dict[str, Dict]],
                       combo_a: str, combo_b: str) -> List[str]:
    """Return query IDs present in BOTH combinations."""
    set_a = set(results.get(combo_a, {}).keys())
    set_b = set(results.get(combo_b, {}).keys())
    return sorted(set_a & set_b)


